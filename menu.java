    public void loadBang() {
        try {
            conn = cn.getConnection();
            int number;
            Vector row;
            String sql = "select*from SANPHAM";
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            ResultSetMetaData metadata = rs.getMetaData();
            number = metadata.getColumnCount();
            tb.setRowCount(0);
            while (rs.next()) {
                row = new Vector();
                for (int i = 1; i <= number; i++) {
                    row.addElement(rs.getString(i));
                }
                tb.addRow(row);
                dgvSP.setModel(tb);
            }

            st.close();
            rs.close();

        } catch (Exception e) {
        }
        dgvSP.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
                if (dgvSP.getSelectedRow() >= 0) {
                    txtMaSP.setText(dgvSP.getValueAt(dgvSP.getSelectedRow(), 0) + "");
                    txtTen.setText(dgvSP.getValueAt(dgvSP.getSelectedRow(), 1) + "");
                    txtTL.setText(dgvSP.getValueAt(dgvSP.getSelectedRow(), 2) + "");
                    txtNCC.setText(dgvSP.getValueAt(dgvSP.getSelectedRow(), 3) + "");
                }
            }
        });
    }

    public void xoatrang() {
        txtMaSP.setText("");
        txtTen.setText("");
        txtTL.setText("");
        txtNCC.setText("");
        loadBang();
    }

