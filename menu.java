    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        try {
            if (txtMaSP.getText().equals("") || txtTen.getText().equals("") || txtTL.getText().equals("") || txtNCC.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Chọn Dữ Liệu Cần Cập Nhập");

            } else {
                Statement st = conn.createStatement();
                String sql = "Update SANPHAM set TenSP='" + txtTen.getText() + "',TheLoai='" + txtTL.getText() + "',TenNCC='" + txtNCC.getText() + "' where MaSP='" + txtMaSP.getText() + "'";
                st = conn.createStatement();
                int kq = st.executeUpdate(sql);
                if (kq > 0) {
                    JOptionPane.showMessageDialog(this, "Cập Nhập Thành Công");
                    xoatrang();
                }
            }

        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnSuaActionPerformed

